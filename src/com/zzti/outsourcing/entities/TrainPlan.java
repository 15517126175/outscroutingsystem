package com.zzti.outsourcing.entities;

public class TrainPlan {
	
	private int trainplan_id;
	private String yearplan;
	private String content;
	public int getTrainplan_id() {
		return trainplan_id;
	}
	public void setTrainplan_id(int trainplan_id) {
		this.trainplan_id = trainplan_id;
	}
	
	public String getYearplan() {
		return yearplan;
	}
	public void setYearplan(String yearplan) {
		this.yearplan = yearplan;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	

}
